    private void tblBvuelosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBvuelosMouseClicked
     //se hace el metodo para si se da click se pongan los datos
        try {
            int fila = tblBvuelos.getSelectedRow();
            int id = Integer.parseInt(tblBvuelos.getValueAt(fila, 0).toString());
            
            
            PreparedStatement ps;
            ResultSet rs;
            AerolineaConexion con = new AerolineaConexion();
            con.conectar();
            String consulta = "SELECT id,pasajero,hora,fecha,nvuelo,destino,asiento,tpago FROM vuelos WHERE id = ?";
            ps = con.conexion.prepareStatement(consulta);
            ps.setInt(1,id);
            
            rs = ps.executeQuery();
            while (rs.next()) {
                this.id.setText(String.valueOf(id));
                this.nombre.setText(rs.getString("pasajero"));
                this.hora.setText(rs.getString("hora"));
                this.fecha.setText(rs.getString("fecha"));
                this.asiento.setText(rs.getString("asiento"));
                
                
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }


    }//GEN-LAST:event_tblBvuelosMouseClicked

