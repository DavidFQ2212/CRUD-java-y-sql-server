    private void cargarTabla() {
        DefaultTableModel modeloTabla = (DefaultTableModel) tblcanselar.getModel();
        modeloTabla.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        int[] anchos = {10, 50, 20, 20, 20, 50, 20, 20};
        for (int i = 0; i < tblcanselar.getColumnCount(); i++) {
            tblcanselar.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        try {
            AerolineaConexion con = new AerolineaConexion();
            con.conectar();
            ps = con.conexion.prepareStatement("SELECT id,pasajero,hora,fecha,nvuelo,destino,asiento,tpago FROM vuelos");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int indice = 0; indice < columnas; indice++) {
                    fila[indice] = rs.getObject(indice + 1);

                }
                modeloTabla.addRow(fila);

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

