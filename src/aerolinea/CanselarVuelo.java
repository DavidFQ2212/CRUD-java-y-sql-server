    private void btncanselarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncanselarActionPerformed
        //se hace la accion de canselar viaje
        int id = Integer.parseInt(txtid.getText());

        try {//el try por la consulta
            AerolineaConexion l = new AerolineaConexion();
            l.conectar();//se crea la onexion
            PreparedStatement ps;
            //se hace la consulta
            ps = l.conexion.prepareStatement("DELETE FROM vuelos Where id = ?");
            ps.setInt(1, id);
            ps.executeUpdate();//se ejecuta el cambio
            JOptionPane.showMessageDialog(null, "EL REGISTRO SE HA ELIMINADO CORRECTAMENTE");//se indica que se realizo con exito
            //se mandan a llamar los metodos de cargar tabla y para limpiar el texto
            limpiar();
            cargarTabla();
            //por si algo falla que indique

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,e);
            

        }


    }//GEN-LAST:event_btncanselarActionPerformed

    private void tblcanselarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblcanselarMouseClicked
       //se hace por si se clique se le asignen los valores en los textfield
        try {

            int fila = tblcanselar.getSelectedRow();

            int id = Integer.parseInt(tblcanselar.getValueAt(fila, 0).toString());
            PreparedStatement ps;
            ResultSet rs;
            AerolineaConexion con = new AerolineaConexion();
            con.conectar();
            String consulta = "SELECT pasajero,hora,fecha,nvuelo,destino,asiento,tpago FROM vuelos WHERE id = ?";
            ps = con.conexion.prepareStatement(consulta);
            ps.setInt(1, id);
            rs = ps.executeQuery();
            while (rs.next()) {
                txtid.setText(String.valueOf(id));
                txtnombre.setText(rs.getString("pasajero"));

                txtfecha.setText(rs.getString("fecha"));

                txtdestino.setText(rs.getString("destino"));
                txtasiento.setText(rs.getString("asiento"));

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_tblcanselarMouseClicked
