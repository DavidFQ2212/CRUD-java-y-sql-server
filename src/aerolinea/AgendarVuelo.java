    private void tblVuelosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVuelosMouseClicked
        //este es el evento por si se clickea en alguna parte de la tabla 
        try {//el try es por la consulta
            int fila = tblVuelos.getSelectedRow();//para ver que esta seleccionano
            int id = Integer.parseInt(tblVuelos.getValueAt(fila, 0).toString());
            
            PreparedStatement ps;
            ResultSet rs;
            AerolineaConexion con = new AerolineaConexion();
            con.conectar();//se realiza la conexion
            //se realiza la consultaa
            String consulta = "SELECT pasajero,hora,fecha,nvuelo,destino,asiento,tpago FROM vuelos WHERE id = ?";
            ps = con.conexion.prepareStatement(consulta);//se prepara la consulta para realizrse
            ps.setInt(1, id);//se evaluara por id
            rs = ps.executeQuery();//se realizara la consulta
            while (rs.next()) {
                //se les asigna lo de la consulta
                txtId.setText(String.valueOf(id));
                txtPasajero.setText(rs.getString("pasajero"));
                txtHora.setText(rs.getString("hora"));
                txtFecha.setText(rs.getString("fecha"));
                txtNumeroV.setText(rs.getString("nvuelo"));
                txtDEstino.setText(rs.getString("destino"));
                txtAsiento.setText(rs.getString("asiento"));
                if (rs.getString("tpago").equals("Tarjeta")) {
                    rbTarjeta.setSelected(true);
                } else if (rs.getString("tpago").equals("Efectivo")) {
                    rbEfectivo.setSelected(true);

                }
            }
        } catch (SQLException e) {
            //por si algo faya que diga el error
            JOptionPane.showMessageDialog(null, e.toString());
        }


    }//GEN-LAST:event_tblVuelosMouseClicked
