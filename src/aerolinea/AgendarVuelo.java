//se hace el metodo para cargar la tabla 
    private void cargarTabla() {
        
        DefaultTableModel modeloTabla = (DefaultTableModel) tblVuelos.getModel();//se pone como default la tabla
        modeloTabla.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columnas;
        //se da los valores para poder darle los espacios a las celdas
        int[] anchos = {10, 50, 20, 20, 20, 50, 20, 20};
        for (int i = 0; i < tblVuelos.getColumnCount(); i++) {
            tblVuelos.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
        }
        try {
            AerolineaConexion con = new AerolineaConexion();
            con.conectar();// se hace conexion  y se realiza consulta
            ps = con.conexion.prepareStatement("SELECT id,pasajero,hora,fecha,nvuelo,destino,asiento,tpago FROM vuelos");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columnas = rsmd.getColumnCount();
            //se hace el while para que arroje todos los resultados de la base de datos 
            while (rs.next()) {
                Object[] fila = new Object[columnas];
                for (int indice = 0; indice < columnas; indice++) {
                    fila[indice] = rs.getObject(indice + 1);

                }
                modeloTabla.addRow(fila);

            }
            //por si algo sale  mal especifique 
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

